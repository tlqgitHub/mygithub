package com.huatandm.meetme.adapter;

public class DocsContant {

	int doc_ID;
	String doc_type, doc_address, doc_title, doc_size, doc_author;

	public int getDoc_ID() {
		return doc_ID;
	}

	public void setDoc_ID(int doc_ID) {
		this.doc_ID = doc_ID;
	}

	public String getDoc_type() {
		return doc_type;
	}

	public void setDoc_type(String doc_type) {
		this.doc_type = doc_type;
	}

	public String getDoc_address() {
		return doc_address;
	}

	public void setDoc_address(String doc_address) {
		this.doc_address = doc_address;
	}

	public String getDoc_title() {
		return doc_title;
	}

	public void setDoc_title(String doc_title) {
		this.doc_title = doc_title;
	}

	public String getDoc_size() {
		return doc_size;
	}

	public void setDoc_size(String doc_size) {
		this.doc_size = doc_size;
	}

	public String getDoc_author() {
		return doc_author;
	}

	public void setDoc_author(String doc_author) {
		this.doc_author = doc_author;
	}
}
